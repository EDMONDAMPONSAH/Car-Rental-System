    private void jTable4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable4MouseClicked
        // TODO add your handling code here:
        jButton1.setEnabled(true);
        jButton2.setEnabled(true);
        DefaultTableModel dft = (DefaultTableModel) jTable4.getModel();
        int selectedIndex = jTable4.getSelectedRow();
        String iddd = dft.getValueAt(selectedIndex, 0).toString();
        try {
            con = database.connectDB();
            pst = con.prepareStatement("select * from car where idd='" + iddd + "'");
            rs = pst.executeQuery();
            if (rs.next()) {
                jLabel4.setText(rs.getString("cbrand"));
                jLabel8.setText(rs.getString("cname"));
                jLabel12.setText(rs.getString("cmodel"));
                jLabel11.setText(rs.getString("cprice"));
                jLabel22.setText(rs.getString("ccolor"));
                jLabel19.setText(rs.getString("ctrans"));
                jLabel21.setText(rs.getString("ctype"));
                byte[] b = rs.getBytes("cpic");
                ImageIcon img = new ImageIcon(new ImageIcon(b).getImage().getScaledInstance(350, 160, Image.SCALE_SMOOTH));
                jLabel3.setIcon(img);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jTable4MouseClicked

