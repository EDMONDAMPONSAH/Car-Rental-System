    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String idd = jLabel14.getText();
        String cbrand = jComboBox1.getSelectedItem().toString();
        String cname = jTextField1.getText();
        String ctype = jComboBox2.getSelectedItem().toString();
        String ctrans = jComboBox3.getSelectedItem().toString();
        String cmodel = jTextField2.getText();
        String cprice = jTextField3.getText();
        String ccolor = jTextField4.getText();
        if (cname.equals("Enter Name")) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Name Field is required</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (cmodel.equals("Enter Year")) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Model Field is required</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (cprice.equals("Enter Price")) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Price Field is required</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (ccolor.equals("Enter Color")) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Color Field is required</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if(jLabel3.getIcon()==null){
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Upload Car Picture</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        }
        
        else {
            try {
                con = database.connectDB();
                pst = con.prepareStatement("insert into car(idd,cbrand,cname,ctype,ctrans,cmodel,cprice,ccolor,availability,cpic)values(?,?,?,?,?,?,?,?,?,?)");
                pst.setString(1, idd);
                pst.setString(2, cbrand);
                pst.setString(3, cname);
                pst.setString(4, ctype);
                pst.setString(5, ctrans);
                pst.setString(6, cmodel);
                pst.setString(7, cprice);
                pst.setString(8, ccolor);
                pst.setString(9, "available");
                pst.setBytes(10, person_image);
                pst.executeUpdate();
                jLabel17.setVisible(true);
                clearFields();
                autoID();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

