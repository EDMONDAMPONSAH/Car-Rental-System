    private void autoID() {
        try {
            con = database.connectDB();
            st = con.createStatement();
            rs = st.executeQuery("select max(idd) from car");
            rs.next();
            rs.getString("max(idd)");
            if (rs.getString("max(idd)") == null) {
                jLabel14.setText("CR001");
            } else {
                Long idd = Long.parseLong(rs.getString("max(idd)").substring(2, rs.getString("max(idd)").length()));
                idd++;
                jLabel14.setText("CR" + String.format("%03d", idd));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                st.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

