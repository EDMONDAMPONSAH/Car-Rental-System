    private void loadTable3(String search) {
        try {
            con = database.connectDB();
            pst = con.prepareStatement("select * from car where availability = 'booked' and name like '" + search + "%'");
            DefaultTableModel dft = (DefaultTableModel) jTable4.getModel();
            dft.setRowCount(0);
            rs = pst.executeQuery();
            while (rs.next()) {
                dft.addRow(new Object[]{
                    rs.getString("idd"),
                    rs.getString("name"),
                    rs.getString("cbrand"),
                    rs.getString("ccolor"),
                    rs.getString("cprice")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

