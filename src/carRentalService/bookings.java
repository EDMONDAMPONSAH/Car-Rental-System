    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel dft = (DefaultTableModel) jTable4.getModel();
            int selectedIndex = jTable4.getSelectedRow();
            String idd = dft.getValueAt(selectedIndex, 0).toString();
            con = database.connectDB();
            pst = con.prepareStatement("update car set availability=? where idd='" + idd + "'");
            pst.setString(1, "available");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:green;\">Successfully Returned</b></html>", "Message", JOptionPane.INFORMATION_MESSAGE);
            loadTable3("");
            resetFields();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                pst.close();
                rs.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

